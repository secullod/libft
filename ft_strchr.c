#include "libft.h"

char	*ft_strchr(char *s, int c)
{
	int	i;

	i = -1;
	while (s[++i])
		if (s[i] == c)
			return (&s[i]);
	if (!c && s[i] == '\0')
		return ((char *)s + i);
	return (NULL);
}
